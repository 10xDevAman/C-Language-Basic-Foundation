//  Write a program to print the size of an int, a float, a char, and a double type variable 

#include<stdio.h>
int main()
{
    printf("\nint : sizeof(int) = %d.", sizeof(int));
    printf("\nfloat : sizeof(float) = %d.", sizeof(float));
    printf("\nchar : sizeof(char) = %d.", sizeof(char));
    printf("\ndouble : sizeof(double) = %d.", sizeof(double));
    return 0;
}